				CLCD_I2C_WriteString(&LCD1, "NhietDo:");
				
				char tempVol[6];
				char tempVal[6];
				sprintf(tempVol, "%f", voltage);
				sprintf(tempVal, "%f", temperature);
				
				CLCD_I2C_SetCursor(&LCD1, 10,0);
