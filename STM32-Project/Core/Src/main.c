  /* USER CODE BEGIN 2 */

CLCD_I2C_Init(&LCD1,&hi2c1,0x4e,20,4);
CLCD_I2C_SetCursor(&LCD1, 0,0);
CLCD_I2C_WriteString(&LCD1, "     Project       ");
HAL_Delay(1000);
HAL_ADC_Start_IT(&hadc1);
  /* USER CODE END 2 */

